money,p50000,p10000,p5000,p1000=0,0,0,0,0

money=int(input('돈을 입력하시오.>>>'))
print('입력된 금액: ' +format(money,',')+ '원')
p50000= money//50000
p10000= money%50000//10000
p5000= money%50000%10000//5000
p1000= money%50000%10000%5000//1000

print('5만원 : {}장 \n1만원 : {}장 \n5천원 : {}장 \n1천원 : {}장'.format(p50000,p10000,p5000,p1000))